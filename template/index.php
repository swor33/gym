<script>
    const hamburger = document.querySelector(".hamburger");
    const navMenu =document.querySelector(".nav-menu");

    hamburger.addEventListener("click", () => {
        hamburger.classList.toggle("active");
        navMenu.classList.toggle("active");
    })

    function toggleText() {
        let more = document.getElementById("more");
        let showMoreText = document.getElementById("moreText");
        let buttonText = document.getElementById("textButton");

        if (showMoreText.style.display === "none") {
            showMoreText.style.display = "inline";
            more.style.display = "none";
            buttonText.innerHTML = "Show Less";
        } else {
            showMoreText.style.display = "none";
            more.style.display = "inline";
            buttonText.innerHTML = "Show More";
        }
    }

    function bgChange(event) {
        let contactSection = document.getElementById("contact");
    
        if (event.target.id === "location" || event.target.id === "phone" || event.target.id === "email") {
            contactSection.style.backgroundColor = "red";
            contactSection.div.backgroundColor ="black";
        } else {
            contactSection.style.backgroundColor = "black"; 
        }
    }
    function resetBgColor() {
        let contactSection = document.getElementById("contact");
        contactSection.style.backgroundColor = "";
    }
    
</script>
